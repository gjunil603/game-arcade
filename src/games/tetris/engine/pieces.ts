const PIECE_TYPES: PieceType[] = ['I', 'O', 'T', 'S', 'Z', 'J', 'L'];

export function getShape(piece: ActivePiece): number[][] {
  return SHAPES[piece.type][piece.rotation % 4];
}

export function getColor(type: PieceType): string {
  return COLORS[type];
}

export function randomPieceType(): PieceType {
  return PIECE_TYPES[Math.floor(Math.random() * PIECE_TYPES.length)];
}

export function createPiece(type: PieceType): ActivePiece {
  return { type, rotation: 0, x: 3, y: 0 };
}

export function getCells(piece: ActivePiece): { x: number; y: number }[] {
  const shape = getShape(piece);
  const cells: { x: number; y: number }[] = [];

  for (let row = 0; row < 4; row++) {
    for (let col = 0; col < 4; col++) {
      if (shape[row][col]) {
        cells.push({ x: piece.x + col, y: piece.y + row });
      }
    }
  }

  return cells;
}
